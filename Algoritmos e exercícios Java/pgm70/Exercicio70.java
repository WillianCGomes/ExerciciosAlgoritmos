package exercicios;

/**
 * Programa para informar a tabuada do 8
 * 
 * @author Willian Carlos Gomes
 * @since 10 de fev. de 2021
 */
public class Exercicio70 {

	/*
	 * Método principal para a execução da classe
	 */
	public static void main(String[] args) {

		System.out.println("Tabuada do 8" + "\n");

		// Laço de repetição atribuído ao cálculo e impressão no console.
		for (int i = 1; i < 11; i++) {
			System.out.println("8x" + i + " = " + i * 8);
		}
	}

}
