package exemplo04;

/**
 * Programa para demonstrar o laço de repetição for
 * 
 * @author Willian Gomes
 * @since 10/02/2021
 */
public class RepeticaoFor {

	/*
	 * Método principal para a execução da classe
	 */
	public static void main(String[] args) {// Início do laço for

		for (int i = 0; i < 10; i++) {
			System.out.println(i + " ao cubo é: " + ((i * i) * i));

		} // Fim do laço for
	}

}
