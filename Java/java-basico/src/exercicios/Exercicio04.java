/**
 * Programa para atribuir valores às variáveis
 * 
 * @author Willian Gomes
 * @since 04/02/2021
 */
public class Exercicio04 {

	/*
	 * Método principal para executar o programa
	 */
	public static void main(String[] args) {
		// declarando uma variável do tipo String para o nome
		String nome;

		// declarando uma variável do tipo String para Celular
		String celular;

		// declarando uma variável do tipo String para a cidade
		String cidade;

		// atribuindo valores para as variáveis
		nome = "Willian Carlos Gomes";
		celular = "(41)9 9804-5682";
		cidade = "Curitiba";

		// exibindo o conteúdo das variáveis no console
		System.out.println("Meu nome é : " + nome + ".");
		System.out.println("Meu celular é : " + celular + ".");
		System.out.println("Minha cidade é : " + cidade + ".");
	}

}
