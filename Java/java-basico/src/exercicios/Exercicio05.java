package exercicios;

/**
 * Atribuindo valores às variáveis
 * 
 * @author Willian Gomes
 * @since 04/02/2021
 */
public class Exercicio05 {

	/*
	 * Método principal para executar o programa
	 */
	public static void main(String[] args) {
		// declarando uma variável do tipo Inteiro para a idade
		int idade;

		// declarando uma variável do tipo Double para a altura
		double altura;

		// declarando uma variável do tipo Double para o peso
		double peso;

		// atribuindo valores para as variáveis
		idade = 18;
		altura = 1.64;
		peso = 58;

		// exibindo o conteúdo das variáveis no console
		System.out.println("Minha idade é : " + idade + ".");
		System.out.println("Minha altura é : " + altura + ".");
		System.out.println("Meu peso é : " + peso + ".");

	}

}
